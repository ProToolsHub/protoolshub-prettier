/**
 * V√©rifie si un fichier doit √™tre trait√© selon son extension
 */
function shouldProcessFile(filePath) {
  const ext = path.extname(filePath).toLowerCase();
  return config.extensions.includes(ext);
}

/**
 * Scanne r√©cursivement un r√©pertoire et traite les fichiers
 */
async function scanDirectory(dir) {
  // V√©rifier si le r√©pertoire existe
  if (!fs.existsSync(dir)) {
    console.error(`‚ùå Le r√©pertoire ${dir} n'existe pas.`);
    return;
  }

  // Lire le contenu du r√©pertoire
  const entries = fs.readdirSync(dir, { withFileTypes: true });

  for (const entry of entries) {
    const entryPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      // Ignorer les r√©pertoires sp√©cifi√©s
      if (config.ignoreDirs.includes(entry.name)) {
        continue;
      }

      // R√©cursion dans les sous-r√©pertoires
      await scanDirectory(entryPath);
    } else if (entry.isFile()) {
      // Traiter le fichier si son extension est dans la liste
      if (shouldProcessFile(entryPath)) {
        stats.scanned++;
        await formatFile(entryPath);
      }
    }
  }
}

