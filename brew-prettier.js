/**
 * Ex√©cute une commande et retourne la promesse
 */
function execCommand(command, args, options = {}) {
  return new Promise((resolve, reject) => {
    const process = spawn(command, args, options);

    let stdout = '';
    let stderr = '';

    process.stdout.on('data', data => {
      stdout += data.toString();
    });

    process.stderr.on('data', data => {
      stderr += data.toString();
    });

    process.on('close', code => {
      if (code === 0) {
        resolve({ stdout, stderr });
      } else {
        reject(new Error(`Commande a √©chou√© avec le code ${code}: ${stderr}`));
      }
    });
  });
}

/**
 * Formate un fichier avec Prettier et les linters appropri√©s
 */
async function formatFile(filePath) {
  const ext = path.extname(filePath).toLowerCase();

  try {
    // Formater avec Prettier
    await formatWithPrettier(filePath);

    // Linting avec ESLint pour JavaScript/TypeScript
    if (config.eslintConfig.enabled && ['.js', '.jsx', '.ts', '.tsx', '.vue'].includes(ext)) {
      await lintWithESLint(filePath);
    }

    // Linting avec Stylelint pour CSS/SCSS
    if (config.stylelintConfig.enabled && ['.css', '.scss', '.sass', '.less'].includes(ext)) {
      await lintWithStylelint(filePath);
    }

    stats.formatted++;
    console.log(`‚úÖ Formatt√©: ${filePath}`);
  } catch (error) {
    console.error(`‚ùå Erreur lors du formatage de ${filePath}: ${error.message}`);
    stats.errors++;
  }
}

/**
 * Formate un fichier avec Prettier
 */
async function formatWithPrettier(filePath) {
  const prettierBin = getPrettierBin();
  const configPath = path.join(PRETTIER_DIR, '.prettierrc');

  try {
    // V√©rifier d'abord si le fichier a besoin d'√™tre formatt√©
    const { stdout } = await execCommand(
      prettierBin,
      ['--check', '--config', configPath, filePath],
      { stdio: ['ignore', 'pipe', 'pipe'] }
    );

    // Si le fichier est d√©j√† bien formatt√©, marquer comme ignor√©
    if (stdout.trim() === '') {
      stats.skipped++;
      return;
    }

    // Le fichier a besoin d'√™tre formatt√©
    await execCommand(prettierBin, ['--write', '--config', configPath, filePath], {
      stdio: ['ignore', 'pipe', 'pipe'],
    });
  } catch (error) {
    // Si l'erreur est li√©e au check, cela signifie que le fichier a besoin d'√™tre formatt√©
    await execCommand(prettierBin, ['--write', '--config', configPath, filePath], {
      stdio: ['ignore', 'pipe', 'pipe'],
    });
  }
}

/**
 * Lint avec ESLint
 */
async function lintWithESLint(filePath) {
  const eslintBin = getESLintBin();
  const configPath = path.join(ESLINT_DIR, '.eslintrc.json');

  try {
    await execCommand(eslintBin, ['--fix', '--config', configPath, filePath], {
      stdio: ['ignore', 'pipe', 'pipe'],
    });
  } catch (error) {
    // Ignorer les erreurs de linting qui ne peuvent pas √™tre corrig√©es automatiquement
    console.warn(`‚ö†Ô∏è Avertissement ESLint pour ${filePath}: ${error.message}`);
  }
}

